name = "Nicolas"
print(len(name))  # Imprime la longitud de la cadena
print(name.find("o"))  # Encuentra la posición de la letra "o"
print(name.capitalize()) # Convierte la primera letra a mayúscula
print(name.upper())  # Convierte la cadena a mayúsculas
print(name.lower())  # Convierte la cadena a minúsculas
print(name.isdigit())  # Verifica si la cadena es un número
print(name.isalpha())  # Verifica si la cadena contiene solo letras
print(name.count("i"))  # Cuenta cuántas veces aparece "i" en la
print(name.replace("s", "a"))  # Reemplaza "s" por "a"
print(name*3)  # Repite la cadena 3 veces